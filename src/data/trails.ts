// Helper to get trail by slug
export function getTrailBySlug(slug: string): Trail | undefined {
  return trails.find((t) => t.slug === slug);
}

// Helper to get nearby trails
export function getNearbyTrails(trailId: string, limit: number = 3): Trail[] {
  const currentTrail = trails.find((t) => t.id === trailId);
  if (!currentTrail) return [];

  return trails
    .filter((t) => t.id !== trailId)
    .sort((a, b) => {
      // Sort by proximity (simple distance comparison)
      const distA = Math.abs(a.distanceFromBend - currentTrail.distanceFromBend);
      const distB = Math.abs(b.distanceFromBend - currentTrail.distanceFromBend);
      return distA - distB;
    })
    .slice(0, limit);
}

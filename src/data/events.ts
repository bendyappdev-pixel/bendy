// Helper to generate weekly recurring events
function generateWeeklyEvents(config: {
  title: string;
  location: string;
  address: string;
  category: Event['category'];
  description: string;
  price: string;
  startDate: Date;
  endDate: Date;
  dayOfWeek: number;
  startId: number;
}): Event[] {
  const events: Event[] = [];
  const current = new Date(config.startDate);
  let id = config.startId;

  while (current <= config.endDate) {
    if (current.getDay() === config.dayOfWeek) {
      events.push({
        id: String(id++),
        title: config.title,
        date: new Date(current),
        location: config.location,
        address: config.address,
        category: config.category,
        description: config.description,
        price: config.price,
      });
    }
    current.setDate(current.getDate() + 1);
  }

  return events;
}

